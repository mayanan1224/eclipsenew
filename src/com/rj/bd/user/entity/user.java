package com.rj.bd.user.entity;

public class user {
	public int id;
	public String name;
	public String sex;
	public int age;
	public String seff;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getSeff() {
		return seff;
	}
	public void setSeff(String seff) {
		this.seff = seff;
	}
	

}
